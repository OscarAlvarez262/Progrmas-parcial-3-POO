    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        // TODO add your handling code here:
        //System.out.println(Lista.size());
        int[] num = new int[Lista.size()];// crear arreglo con el tamalo de los elementos de la lista

        for (int i = 0; i < Lista.size(); i++) {
            num[i] = (int) Lista.getElementAt(i);// ingresar convertido en entero los valores de la lista al arreglo
        }
        int aux = 0;
        for (int i = 0; i < num.length; i++) {
            for (int j = 0; j < num.length - 1; j++) {
                if (num[j] < num[j + 1]) {
                    aux = num[j + 1];
                    num[j + 1] = num[j];
                    num[j] = aux;
                }
            }
        }
        Lista.clear();
        for (int i = 0; i < num.length; i++) {
            //System.out.println(num[i]);// mostrar los elementos del arreglo de la posicion int
        Lista.addElement(num[i]); // AÑADIR ELEMENTO
            ListaNumeros.setModel(Lista);
        }
        // System.out.println(Lista.getElementAt(2)); 
    }//GEN-LAST:event_btnOrdenarActionPerformed

