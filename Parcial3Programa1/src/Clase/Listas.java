    private void Agregar() {
        if (txtNumero.getText().length() == 0) {// VALIDACION PARA NO AGREGAR EN BLANCO
            JOptionPane.showMessageDialog(null, "Ingresa un numero", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {

            int Numero = Integer.parseInt(txtNumero.getText());
            Lista.addElement(Numero); // AÑADIR ELEMENTO
            ListaNumeros.setModel(Lista); // INGRESAR EN LA LISTA
            txtNumero.setText("");// LIMPIAR
        }
    }

    private void Eliminar() {
        index = ListaNumeros.getSelectedIndex();//Definir que index es la opcion selecionada de la lista
        if (index != -1) {
            Lista.removeElementAt(index);//Eliminar elemento de la opcion selecionada
        } else {
            JOptionPane.showMessageDialog(null, "Lista vacia", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

